  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  mge_event_id bigint NOT NULL REFERENCES public.mge_events(id) ON DELETE CASCADE,
  commander_name text NOT NULL,
  is_focus boolean NOT NULL DEFAULT false,
  sort_order int NOT NULL DEFAULT 0,
  UNIQUE(mge_event_id, commander_name)
);

-- MGE Rank Tiers (admin-defined rank structure with point caps)
CREATE TABLE IF NOT EXISTS public.mge_rank_tiers (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  mge_event_id bigint NOT NULL REFERENCES public.mge_events(id) ON DELETE CASCADE,
  tier_label text NOT NULL,
  point_cap bigint,
  is_ffa boolean NOT NULL DEFAULT false,
  sort_order int NOT NULL DEFAULT 0,
  UNIQUE(mge_event_id, tier_label)
);

-- MGE Applications (player submissions with commander stats)
CREATE TABLE IF NOT EXISTS public.mge_applications (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  mge_event_id bigint NOT NULL REFERENCES public.mge_events(id) ON DELETE CASCADE,
  applicant_name text NOT NULL,
  applicant_alliance text,
  applicant_power bigint,
  commander_name text NOT NULL,
  commander_level int,
  skill_levels int[],
  commander_stars int,
  preferred_tier text,
  max_tier text,
  notes text,
  status text NOT NULL DEFAULT 'pending',
  officer_notes text,
  assigned_tier text,
  decided_at timestamptz,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now(),
  UNIQUE(mge_event_id, applicant_name)
);

